// logical operator //

// logical operator ----->  AND && , OR || , NOT ! ,
//  AND  &&
/*  true && true  = true 
    true && false =false 
    false && true =false
    false && false=false */

    console.log(12>10 && 13==13) // true
    console.log(1!=='1'&& 1===1) // true
    console.log(8<=10 && 20==='20') //false
    console.log(2!==3 && true)   //true

// OR operator -------> 
/* true || true = true
   true || false = true
   false || true = true 
   false  || false = false  */

   console.log(1>=0 || 2>=20)  //true
   console.log(12>10 || 13!==13) // true
   console.log(20==21 || true) // true
   console.log(8<=10 || 20==='20') //true
    console.log(2!==2 || false)   //false

    // NOT -----> for converting true-->false & false-->true
    //  !
     console.log(!true)  //false
     console.log(!false) //true
     console.log(10!=='10')  // true
     


